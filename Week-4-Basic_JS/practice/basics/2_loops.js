// for loop :

console.log("for loop");
let a = 5
for (let i = 0; i < a; i++) {
    console.log(i);
}

// while loop
console.log("while loop");
let i = 0
while(i < a){
    console.log(i);
    i+=1
}

// do-while loop
console.log("do-while loop");
let j = 0
do{
    console.log(j);
    j++
}while(j<a)

